{{-- 
    Dark Mode Classes Helper Component
    This component provides a centralized way to apply dark mode classes
    Usage: @include('components.dark-mode-classes')
--}}

{{-- 
    Common Dark Mode Class Patterns:
    
    Background Colors:
    - bg-white → dark:bg-slate-800
    - bg-gray-50 → dark:bg-slate-900
    - bg-gray-100 → dark:bg-slate-700
    
    Text Colors:
    - text-gray-900 → dark:text-slate-100
    - text-gray-600 → dark:text-slate-400
    - text-gray-400 → dark:text-slate-500
    
    Border Colors:
    - border-gray-200 → dark:border-slate-700
    - border-gray-300 → dark:border-slate-600
    
    Hover States:
    - hover:bg-gray-100 → dark:hover:bg-slate-700
    - hover:text-gray-600 → dark:hover:text-slate-300
    
    Status Badges:
    - bg-yellow-100 text-yellow-800 → dark:bg-yellow-900 dark:text-yellow-200
    - bg-green-100 text-green-800 → dark:bg-green-900 dark:text-green-200
    - bg-blue-100 text-blue-800 → dark:bg-blue-900 dark:text-blue-200
    - bg-red-100 text-red-800 → dark:bg-red-900 dark:text-red-200
    
    Icon Backgrounds:
    - bg-blue-100 → dark:bg-blue-900
    - bg-green-100 → dark:bg-green-900
    - bg-yellow-100 → dark:bg-yellow-900
    - bg-purple-100 → dark:bg-purple-900
    
    Icon Colors:
    - text-blue-600 → dark:text-blue-400
    - text-green-600 → dark:text-green-400
    - text-yellow-600 → dark:text-yellow-400
    - text-purple-600 → dark:text-purple-400
--}}

